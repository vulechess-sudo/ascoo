// Trenutni jezik
let currentLang = localStorage.getItem('ascoLang') || 'en';

// Funkcija za prevođenje
function t(key) {
    return translations[currentLang][key] || translations['en'][key] || key;
}

// Promena jezika
function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('ascoLang', lang);
    updatePageLanguage();
    location.reload(); // Reload za sve promene
}

// Ažuriranje svih elemenata
function updatePageLanguage() {
    document.querySelectorAll('[data-translate]').forEach(el => {
        const key = el.dataset.translate;
        const translation = t(key);
        
        // Ako sadrži <br>, koristi innerHTML
        if (translation.includes('<br>')) {
            el.innerHTML = translation;
        } else {
            el.textContent = translation;
        }
    });
    
    // Placeholders
    document.querySelectorAll('[data-translate-placeholder]').forEach(el => {
        el.placeholder = t(el.dataset.translatePlaceholder);
    });
    
    // Dugme za jezik
    const langBtn = document.querySelector('.language-dropdown .dropbtn');
    if (langBtn) {
        langBtn.innerHTML = `${currentLang.toUpperCase()} <i class="fas fa-chevron-down"></i>`;
    }
}

// Inicijalizacija
document.addEventListener('DOMContentLoaded', () => {
    updatePageLanguage();
    
    // Dropdown za jezik
    const langDropdown = document.querySelector('.language-dropdown');
    if (langDropdown) {
        const btn = langDropdown.querySelector('.dropbtn');
        const content = langDropdown.querySelector('.dropdown-content');
        
        btn?.addEventListener('click', (e) => {
            e.stopPropagation();
            content?.classList.toggle('show');
        });
        
        document.addEventListener('click', () => {
            content?.classList.remove('show');
        });
    }
});